// Addition
exports.add = function(value_1, value_2) {
    return `Operation is: Addition // Value 1: ${value_1} // Value 2: ${value_2} // Result is: ${value_1+value_2}`
};

// Substraction
exports.sub = function(value_1, value_2) {
    return `Operation is: Substract // Value 1: ${value_1} // Value 2: ${value_2} // Result is: ${value_1-value_2}`
}; 

// Multiplication
exports.multiply = function(value_1, value_2) {
    return `Operation is: Multiply // Value 1: ${value_1} // Value 2: ${value_2} // Result is: ${value_1*value_2}`
}; 

// Division
exports.divide = function(value_1, value_2) {
    return `Operation is: Division // Value 1: ${value_1} // Value 2: ${value_2} // Result is: ${value_1/value_2}`
};


















